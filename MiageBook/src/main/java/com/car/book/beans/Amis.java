package com.car.book.beans;

public class Amis {
	
	private int profils;
	private int amis;
	
	
	public Amis() {
		
	}
	public Amis(int profils, int amis) {
		this.profils = profils;
		this.amis = amis;
	}

	public int getProfils() {
		return profils;
	}
	
	public void setProfils(int profils) {
		this.profils = profils;
	}
	
	public int getAmis() {
		return amis;
	}
	
	public void setAmis(int amis) {
		this.amis = amis;
	}
	
	

}
